package com.basicop;

public class Class2Unary {
	public static void main(String[] args) {
		int a = 123;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
			
		int b = 9;
		System.out.println(b++);
		System.out.println(--b);
		System.out.println(b--);
		System.out.println(++b);
	}
}